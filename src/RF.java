public class RF {
    private int m;
    private int n;

    public RF(int m, int n) {
        //if (n==0) throw new ArithmeticException();
        this.m = m;
        this.n = n;
    }

    public int getM() {
        return m;
    }

    public int getN() {
        return n;
    }
}
